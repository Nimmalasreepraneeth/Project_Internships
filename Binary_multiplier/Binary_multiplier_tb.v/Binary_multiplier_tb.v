`timescale 1ns/1ps

module Multiplier_2bit_tb;

    reg  [1:0] A;
    reg  [1:0] B;
    wire [3:0] Y;

    // Instantiate the DUT
    Multiplier_2bit uut (
        .A(A),
        .B(B),
        .Y(Y)
    );

    initial begin

        $display("\n========= 2-BIT MULTIPLIER TEST =========\n");
        $display("   A    B    |   Y");
        $display("----------------------");

        //=========================================
        // TEST CASE 1 : 0 × 0
        //=========================================
        A = 2'b00; B = 2'b00; #10;

        //=========================================
        // TEST CASE 2 : 0 × 1
        //=========================================
        A = 2'b00; B = 2'b01; #10;

        //=========================================
        // TEST CASE 3 : 0 × 2
        //=========================================
        A = 2'b00; B = 2'b10; #10;

        //=========================================
        // TEST CASE 4 : 0 × 3
        //=========================================
        A = 2'b00; B = 2'b11; #10;

        //=========================================
        // TEST CASE 5 : 1 × 0
        //=========================================
        A = 2'b01; B = 2'b00; #10;

        //=========================================
        // TEST CASE 6 : 1 × 1
        //=========================================
        A = 2'b01; B = 2'b01; #10;

        //=========================================
        // TEST CASE 7 : 1 × 2
        //=========================================
        A = 2'b01; B = 2'b10; #10;

        //=========================================
        // TEST CASE 8 : 1 × 3
        //=========================================
        A = 2'b01; B = 2'b11; #10;

        //=========================================
        // TEST CASE 9 : 2 × 0
        //=========================================
        A = 2'b10; B = 2'b00; #10;

        //=========================================
        // TEST CASE 10 : 2 × 1
        //=========================================
        A = 2'b10; B = 2'b01; #10;

        //=========================================
        // TEST CASE 11 : 2 × 2
        //=========================================
        A = 2'b10; B = 2'b10; #10;

        //=========================================
        // TEST CASE 12 : 2 × 3
        //=========================================
        A = 2'b10; B = 2'b11; #10;

        //=========================================
        // TEST CASE 13 : 3 × 0
        //=========================================
        A = 2'b11; B = 2'b00; #10;

        //=========================================
        // TEST CASE 14 : 3 × 1
        //=========================================
        A = 2'b11; B = 2'b01; #10;

        //=========================================
        // TEST CASE 15 : 3 × 2
        //=========================================
        A = 2'b11; B = 2'b10; #10;

        //=========================================
        // TEST CASE 16 : 3 × 3
        //=========================================
        A = 2'b11; B = 2'b11; #10;

        $display("\n========= SIMULATION COMPLETED =========\n");

        $finish;

    end

    initial begin
        $monitor("Time=%0t | A=%b (%0d) | B=%b (%0d) | Y=%b (%0d)",
                 $time, A, A, B, B, Y, Y);
    end

endmodule


